# Variance
import statistics

statistics.variance([1, 3, 4, 2, 6, 5, 3, 4, 5, 2])

print(statistics.pvariance([1, 3, 4, 2, 6, 5, 3, 4, 5, 2]))

# Standard Deviation
print(statistics.pstdev([1, 3, 4, 2, 6, 5, 3, 4, 5, 2]))

import math

print(math.sqrt(statistics.pvariance([1, 3, 4, 2, 6, 5, 3, 4, 5, 2])))

